package Model;

public class Player extends Entity {
	public static int posX;
	public static int posY;

	public int getPosX() {
		return posX;
	}

	public void setPosX(int xPos) {
		this.posX = posX;
	}

	public int getyPos() {
		return posY;
	}

	public void setyPos(int yPos) {
		this.posY = posY;
	}

}
